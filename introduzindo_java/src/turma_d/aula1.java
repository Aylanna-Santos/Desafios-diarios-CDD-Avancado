package turma_d;

public class aula1 {

	public static void main(String[] args) {
		int x = 10;
		System.out.println(x++); //imprime o número atual e depois soma
		System.out.println(++x); //soma o número e depois imprime
		System.out.println(x--); //imprime o número atual e depois subtrai
		System.out.println(--x); //subtrai o número e depois imprime

	}

}
