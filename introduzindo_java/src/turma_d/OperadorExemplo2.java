package turma_d;

public class OperadorExemplo2 {

	public static void main(String[] args) {
		int a = 10;
		int b = -10;
		boolean c = true;
		boolean d = false;
		System.out.println(~a); //inverte o valor e subtrai 1
		System.out.println(~b); //inverte o valor e subtrai 1
		System.out.println(!c); //inverte o boolean
		System.out.println(!d); //inverte o boolean

	}

}
